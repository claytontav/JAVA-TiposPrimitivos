class TiposPrimitivos{
    public static void main(String[] args){
         
        int idade = 26;
        double valor = 2.90;
        float valor02 = 3.95f;
        String nome = "Clayton";
        boolean verdadeiro = true;
        boolean falso = false;
        char o = 'o';

        System.out.println("Sua idade é: " + idade);
        System.out.println("O primeiro valor é " + valor);
        System.out.println("O segundo valor é: " + valor02);
        System.out.println("Seu nome é: " + nome);
        System.out.println("O valor verdadeiro é: " + verdadeiro);
        System.out.println("O valor de falso é: " + falso);
        System.out.println("O valor do caracter é: " + o);
    }
}